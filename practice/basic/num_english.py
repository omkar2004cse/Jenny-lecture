# enter a single digit number and print its english word 
number=int(input("Enter a Single digit Number:-"))
if number==0:
    print(number,"==ZERO")
elif number==1:
    print(number,"==ONE")
elif number==2:
    print(number,"==TWO")
elif number==3:
    print(number,"==THREE")
elif number==4:
    print(number,"==FOUR")
elif number==5:
    print(number,"==FIVE")
elif number==6:
    print(number,"==SIX")
elif number==7:
    print(number,"==SEVEN")
elif(number==8):
    print(number,"==EIGHT")
elif(number==9):
    print(number,"==NINE")
else:
    print("Enter a Single Digit Number(0 to 9)")